package org.datn.bookstation.entity.enums;

/**
 * Enum định nghĩa các hình thức của sách
 */
public enum BookFormat {
    HARDCOVER,      // Bìa cứng
    PAPERBACK,      // Bìa mềm
    AUDIOBOOK,      // Sách nói
    EBOOK,          // Sách điện tử
    MAGAZINE,       // Tạp chí
    COMIC,          // Truyện tranh
    TEXTBOOK,       // Sách giáo khoa
    NOTEBOOK,       // Sổ tay
    JOURNAL,        // Nhật ký
    WORKBOOK        // Sách bài tập
}
